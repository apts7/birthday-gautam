// Confetti blast using Canvas Confetti
window.onload = () => {
  confetti({
    particleCount: 500,
    spread: 200,
    origin: { y: 0.6 },
  });

  setTimeout(() => {
    confetti({
      particleCount: 250,
      spread: 200,
      origin: { y: 0.4 },
    });
  }, 1000);
};
